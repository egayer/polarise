# Check which optional packages are available at import time.
# Uses find_spec (no actual import) so it's fast and side-effect free.
import importlib.util as _iutil
_AVAILABLE_PACKAGES: dict[str, bool] = {
    pkg: _iutil.find_spec(pkg) is not None
    for pkg in ('cmcrameri', 'colorspace', 'colorcet', 'matplotlib')
}


def interpolate_color(stops: list[tuple[float, float, float]], value: float) -> str:
    """Linearly interpolate color from stops.

    Parameters:
        stops: List of RGB tuples (each value 0-1)
        value: Position to interpolate (0-1)

    Returns:
        Hex color string

    Examples:
        >>> interpolate_color([(1, 0, 0), (0, 0, 1)], 0.5)
        '#7F007F'
    """
    # Clamp value to [0, 1]
    value = max(0.0, min(1.0, value))

    # Handle edge cases
    if value == 0.0:
        r, g, b = stops[0]
        return rgb_to_hex(int(r * 255), int(g * 255), int(b * 255))
    if value == 1.0:
        r, g, b = stops[-1]
        return rgb_to_hex(int(r * 255), int(g * 255), int(b * 255))

    # Find surrounding stops
    n_stops = len(stops)
    segment_size = 1.0 / (n_stops - 1)
    segment_idx = int(value / segment_size)

    # Clamp segment index
    segment_idx = min(segment_idx, n_stops - 2)

    # Get surrounding stops
    stop1 = stops[segment_idx]
    stop2 = stops[segment_idx + 1]

    # Local interpolation within segment
    local_value = (value - segment_idx * segment_size) / segment_size

    # Interpolate RGB
    r = stop1[0] + (stop2[0] - stop1[0]) * local_value
    g = stop1[1] + (stop2[1] - stop1[1]) * local_value
    b = stop1[2] + (stop2[2] - stop1[2]) * local_value

    return rgb_to_hex(int(r * 255), int(g * 255), int(b * 255))


def get_colormap(cmap: Union[str, Any]) -> Callable[[float], str]:
    """Get colormap function from name or matplotlib-like colormap object.

    Parameters:
        cmap: Colormap name (str) or matplotlib-like colormap object
              (e.g., 'viridis', 'RdBu', cm.jet, cmocean.cm.thermal)

    Returns:
        Function that maps value in [0, 1] to hex color

    Raises:
        ValueError: If colormap not found or invalid type

    Examples:
        >>> # Using string name
        >>> cmap = get_colormap('viridis')
        >>> cmap(0.5)
        '#21908C'

        >>> # Using matplotlib colormap object
        >>> from matplotlib import cm
        >>> cmap = get_colormap(cm.jet)
        >>> cmap(0.5)
        '#00FFFF'
    """
    # Check if it's a colormap object (duck typing)
    # Matplotlib colormaps (and cmocean, colorcet, etc.) are callable
    if hasattr(cmap, '__call__') and not isinstance(cmap, str):
        # It's a matplotlib-like colormap object
        def mapper(value: float) -> str:
            """Wrapper for matplotlib-like colormap object."""
            rgba = cmap(float(value))
            # Handle RGBA tuple (r, g, b, a) or (r, g, b)
            r, g, b = rgba[0], rgba[1], rgba[2]
            return rgb_to_hex(int(r * 255), int(g * 255), int(b * 255))
        return mapper

    # Otherwise, treat as string name
    if isinstance(cmap, str):
        # 1. Check built-in colormaps first
        if cmap in BUILTIN_CMAPS:
            stops = BUILTIN_CMAPS[cmap]
            return lambda v: interpolate_color(stops, v)

        # 2. Check external colormap registry
        # Strip _r suffix for lookup; reverse result afterwards if needed
        reversed_ = cmap.endswith("_r")
        base_name = cmap[:-2] if reversed_ else cmap

        for pkg_name, known_names in EXTERNAL_CMAPS.items():
            if base_name in known_names:
                if not _AVAILABLE_PACKAGES.get(pkg_name, False):
                    raise ValueError(
                        f"Colormap '{cmap}' requires {pkg_name} — "
                        f"pip install {pkg_name}"
                    )
                try:
                    if pkg_name == "cmcrameri":
                        import cmcrameri.cm as _cm
                        cmap_obj = getattr(_cm, base_name, None)
                        if cmap_obj is None:
                            raise ValueError(
                                f"'{base_name}' not found in cmcrameri. "
                                f"Update cmcrameri: pip install --upgrade cmcrameri"
                            )
                        def _make_crameri_mapper(obj: Any, rev: bool) -> Callable[[float], str]:
                            def _mapper(value: float) -> str:
                                if rev:
                                    value = 1.0 - value
                                rgba = obj(float(value))
                                r, g, b = rgba[0], rgba[1], rgba[2]
                                return rgb_to_hex(int(r * 255), int(g * 255), int(b * 255))
                            return _mapper
                        return _make_crameri_mapper(cmap_obj, reversed_)
                    elif pkg_name == "colorspace":
                        from colorspace import sequential_hcl, diverging_hcl, hcl_palettes
                        type_map = {p.name(): p.type() for p in hcl_palettes().get_palettes()}
                        pal_type = type_map.get(base_name, "")
                        if "Diverging" in pal_type:
                            hex_colors = diverging_hcl(base_name)(256)
                        else:
                            hex_colors = sequential_hcl(base_name)(256)
                        stops = [
                            (int(h[1:3], 16) / 255, int(h[3:5], 16) / 255, int(h[5:7], 16) / 255)
                            for h in hex_colors
                        ]
                        def _make_stops_mapper(s: list, rev: bool) -> Callable[[float], str]:
                            def _mapper(value: float) -> str:
                                if rev:
                                    value = 1.0 - value
                                return interpolate_color(s, value)
                            return _mapper
                        return _make_stops_mapper(stops, reversed_)
                    elif pkg_name == "colorcet":
                        import colorcet as _cc
                        hex_list = getattr(_cc, base_name, None)
                        if hex_list is None or not isinstance(hex_list, list):
                            raise ValueError(
                                f"'{base_name}' not found in colorcet. "
                                f"Update colorcet: pip install --upgrade colorcet"
                            )
                        stops = [
                            (int(h[1:3], 16) / 255, int(h[3:5], 16) / 255, int(h[5:7], 16) / 255)
                            for h in hex_list
                        ]
                        def _make_colorcet_mapper(s: list, rev: bool) -> Callable[[float], str]:
                            def _mapper(value: float) -> str:
                                if rev:
                                    value = 1.0 - value
                                return interpolate_color(s, value)
                            return _mapper
                        return _make_colorcet_mapper(stops, reversed_)
                except ImportError:
                    raise ValueError(
                        f"Colormap '{cmap}' requires {pkg_name} — "
                        f"pip install {pkg_name}"
                    )

        # 3. Try matplotlib if available
        if not _AVAILABLE_PACKAGES.get('matplotlib', False):
            builtin_names = [k for k in BUILTIN_CMAPS if not k.endswith('_r')]
            raise ValueError(
                f"Colormap '{cmap}' not found. "
                f"Built-in colormaps: {builtin_names}. "
                f"Install optional packages for more: pip install matplotlib cmcrameri colorcet colorspace"
            )
        try:
            import matplotlib.pyplot as plt

            mpl_cmap = plt.colormaps[cmap]

            def mpl_wrapper(value: float) -> str:
                """Wrapper for matplotlib colormap."""
                rgba = mpl_cmap(value)
                r, g, b = rgba[0], rgba[1], rgba[2]
                return rgb_to_hex(int(r * 255), int(g * 255), int(b * 255))

            return mpl_wrapper

        except ImportError:
            raise ValueError(
                f"Colormap '{cmap}' not found. Built-in colormaps: "
                f"{[k for k in BUILTIN_CMAPS if not k.endswith('_r')]}. "
                f"Install matplotlib for additional colormaps."
            )
        except Exception as e:
            raise ValueError(f"Error loading colormap '{cmap}': {e}")

    # Invalid type
    raise TypeError(
        f"cmap must be a string name or a callable colormap object, got {type(cmap).__name__}"
    )
